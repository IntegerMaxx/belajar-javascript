// MEMBUAT SEBUAH FUNCTION UNTUK KONVERSI SUHU
function convertCelsiusToFahrenheit(temperature){
    const temperatureInFahrenheit = 9 / 5 * temperature + 32;

    console.log('Hasil konversi', temperatureInFahrenheit);
}

